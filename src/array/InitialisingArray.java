package array;

public class InitialisingArray {
    public static void main(String[] args) {
        int a[]={56,48,68,5,5,56945,65,58,5846,5855,458,5,8,6,12,5,25,78};
        int n= a.length;
        System.out.println(n);
        for(int i=1;i<n;i++){
            System.out.print(a[n]);
        }
    }
}
